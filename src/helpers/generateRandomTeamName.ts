export const generateOkkoTeamName = (usedNames: Set<string> = new Set()): string => {
    const available = names.filter(name => !usedNames.has(name));
    if (available.length === 0) {
        return `Команда ${usedNames.size + 1}`;
    }
    const idx = Math.floor(Math.random() * available.length);
    const name = available[idx];
    usedNames.add(name);
    return name;
};
